package misc;

public class Product {

    String colour;
    int price;
    String brand;

    public Product(String colour, int price, String brand) {
        this.colour = colour;
        this.price = price;
        this.brand = brand;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
}
